package ru.klavogonki.kgparser;

/**
 * Тип нестандартного словаря
 */
public enum NonStandardDictionaryType {

    /**
     * Слова.
     */
    words,

    /**
     * Фразы.
     */
    phrases,

    /**
     * Тексты.
     */
    texts,

    /**
     * URL.
     */
    url,

    /**
     * Книга.
     */
    book,

    /**
     * Генератор.
     */
    generator,

    ;
}
